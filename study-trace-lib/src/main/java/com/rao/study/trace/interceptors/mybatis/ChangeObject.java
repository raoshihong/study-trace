package com.rao.study.trace.interceptors.mybatis;

public class ChangeObject {
	
	public String name;
	public Object value;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Object getValue() {
		return value;
	}

	public void setValue(Object value) {
		this.value = value;
	}

}
